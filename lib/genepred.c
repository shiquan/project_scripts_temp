void genepred_clear(struct genepred *line)
{
    if ( line->clear == 1 )
	return;
    if (line->clear == 0) {
	free(line->chrom);
	free(line->name1);
	if ( line->name2 )
	    free(line->name2);
	free(line->exons[0]);
        free(line->exons[1]);
	free(line->dna_ref_offsets[0]);
        free(line->dna_ref_offsets[1]);
        free(line->loc[0]);
        free(line->loc[1]);
    }
    memset(line, 0, sizeof(struct genepred));
    line->clear = 1;
}

tbx_t *load_genepred_file(const char *fn)
{
    return tbx_index_load(fn);
}

